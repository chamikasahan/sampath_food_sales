    public void redirectToInput() {
        try {

            conn = database.connect();

            stm = conn.createStatement();
            String sql = "select * from signup_details";
            ResultSet rs = stm.executeQuery(sql);

            int count = 0;
            while (rs.next()) {
                count++;
            }

            if (count == 0) {
                desktopPaneHolder.removeAll();
                LoginSection ls = new LoginSection();
                desktopPaneHolder.add(ls).setVisible(true);
            } else {
                desktopPaneHolder.removeAll();
                Set1stUser ss = new Set1stUser();
                desktopPaneHolder.add(ss).setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

